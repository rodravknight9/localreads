REGISTRY = build_registry()


def generate_all() -> int:
    count = 0
    for issue in REGISTRY:
        folder = ISSUES_ROOT / f"ISSUE-{issue['id']}"
        shots = folder / "screenshots"
        shots.mkdir(parents=True, exist_ok=True)
        files = {
            folder / "issue.md": render_issue_md(issue),
            folder / "analysis.md": render_analysis_md(issue),
            folder / "fix.md": render_fix_md(issue),
            folder / "resolution.md": render_resolution_md(issue),
            shots / ".gitkeep": GITKEEP_LINE,
        }
        for path, content in files.items():
            path.write_text(content, encoding="utf-8")
            count += 1
    return count


if __name__ == "__main__":
    total = generate_all()
    print(f"Generated {total} files across {len(REGISTRY)} issues.")
