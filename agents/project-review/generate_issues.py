    return {
        "id": id_,
        "severity": severity,
        "title": title,
        "files": files,
        "summary": summary,
        "related": related,
        "analysis": {
            "root_cause": root_cause,
            "code_refs": code_refs,
            "impact": impact,
            "verify": verify,
        },
        "fix": fix,
        "resolution": {"steps": steps, "effort": effort, "phase": phase},
    }


