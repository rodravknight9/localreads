def _table_row(cells: list[str]) -> str:
    return "| " + " | ".join(cells) + " |"


def render_issue_md(issue: dict) -> str:
    related = ", ".join(f"ISSUE-{r}" for r in issue.get("related", [])) or "-"
    files = ", ".join(f"`{f}`" for f in issue["files"])
    lines = [
        f"# ISSUE-{issue['id']}: {issue['title']}",
        "",
        "| Field | Value |",
        "| --- | --- |",
        _table_row(["ID", f"ISSUE-{issue['id']}"]),
        _table_row(["Severity", issue["severity"]]),
        _table_row(["Status", "Open"]),
        _table_row(["Affected files", files]),
        _table_row(["Summary", issue["summary"]]),
        _table_row(["Related issues", related]),
        "",
    ]
    return "\n".join(lines)


def render_analysis_md(issue: dict) -> str:
    a = issue["analysis"]
    refs = "\n".join(f"- {r}" for r in a.get("code_refs", []))
    parts = [
        f"# Analysis - ISSUE-{issue['id']}",
        "",
        "## Root cause",
        a["root_cause"],
        "",
        "## Code references",
        refs,
        "",
        "## Impact",
        a["impact"],
        "",
        "## How to verify",
        a["verify"],
        "",
    ]
    return "\n".join(parts)


def render_fix_md(issue: dict) -> str:
    return f"# Proposed fix - ISSUE-{issue['id']}\n\n{issue['fix'].strip()}\n"


def render_resolution_md(issue: dict) -> str:
    r = issue["resolution"]
    steps = "\n".join(f"- [ ] {s}" for s in r["steps"])
    parts = [
        f"# Resolution plan - ISSUE-{issue['id']}",
        "",
        "## Next steps",
        steps,
        "",
        "## Estimated effort",
        r["effort"],
        "",
        "## Roadmap phase",
        r["phase"],
        "",
        "Status: Planned",
        "",
    ]
    return "\n".join(parts)


